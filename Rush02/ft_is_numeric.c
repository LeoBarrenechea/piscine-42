#include "ft_rush02.h"

int	ft_is_numeric(char *str)
{
	int	n;

	n = 0;
	while (str[n] != '\0')
	{
		if (!((str[n] >= '0') && (str[n] <= '9')))
			return (0);
		n++;
	}
	return (1);
}
